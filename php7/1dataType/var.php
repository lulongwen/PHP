
<?php
    /* 变量就是把右边的值赋给左边
        PHP变量的特点：
        变量没有 var 关键词，变量名要有语义化
        变量必须以 $开头，如变量x 必须要写成 $x
        变量首字母不能以数字开头，但是数字可以夹在变量名中间和结尾
        变量名区分大小写
        变量不要使用特殊符号，中文，不能用中划线，可以用英文输入法的下划线  _
     */
    
    $x = 5;
    $y = 6;
    $x =20;

    $result = $x + $y;

    // echo 输出的意思
    echo $result;


    // 单行注释
    # 单行注释，用的少

    /* 
        多行注释
        PHP注释和JS注释一样，注释通常写在代码上面
     */

?>